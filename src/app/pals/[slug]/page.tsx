interface Params {
  slug: string;
}

/**
 * Pre-render every Pal at build time. This is what makes the per-Pal pages
 * SEO-viable — they ship as static HTML with full metadata.
 */
export function generateStaticParams(): Params[] {
  return allPals.map((p) => ({ slug: p.slug }));
}

export function generateMetadata({ params }: { params: Params }): Metadata {
  const pal = getPalBySlug(params.slug);
  if (!pal) {
    return { title: "Pal not found" };
  }
  const elements = pal.elements.join(" / ");
  // SERP-optimized description: keep the species + paldex up front, end with
  // the action ("breeding combinations") to nudge clicks.
  const description = `${pal.name} (Paldex #${pal.paldexNo}) is a ${elements} type Palworld Pal with power value ${pal.powerValue}. See every parent pair, expected egg counts, and breeding plans.`;
  return buildMetadata({
    title: pal.name,
    description,
    canonical: `/pals/${pal.slug}`,
    ogType: "article",
    // Per-Pal OG image is generated by the sibling opengraph-image.tsx route;
    // Next.js wires it automatically via metadata so we don't need to set
    // `ogImage` here.
  });
}


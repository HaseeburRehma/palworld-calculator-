/* -------------------------------------------------------------------------- */
/*  Helpers                                                                   */
/* -------------------------------------------------------------------------- */

function resolvePairs(child: Pal): Array<{ parentA: Pal; parentB: Pal }> {
  const palMap = new Map(allPals.map((p) => [p.id, p]));
  return getParentPairsFor(child.id)
    .map((pair) => ({
      parentA: palMap.get(pair.parentA),
      parentB: palMap.get(pair.parentB),
    }))
    .filter(
      (p): p is { parentA: Pal; parentB: Pal } =>
        Boolean(p.parentA) && Boolean(p.parentB),
    );
}

/**
 * "Related" = same primary element OR within ±10% power value, ranked by
 * Paldex distance from this Pal. Excludes the Pal itself. Capped at `n`.
 *
 * This is internal-linking gold — every Pal page outbound-links to ~6 other
 * Pal pages, lifting the whole subgraph's link equity.
 */
function pickRelatedPals(pal: Pal, n: number): Pal[] {
  const sameElement = allPals.filter(
    (p) => p.id !== pal.id && p.elements[0] === pal.elements[0],
  );
  const similarPower = allPals.filter(
    (p) => p.id !== pal.id && Math.abs(p.powerValue - pal.powerValue) <= pal.powerValue * 0.1,
  );
  const candidatesById = new Map<string, Pal>();
  for (const p of [...sameElement, ...similarPower]) {
    if (!candidatesById.has(p.id)) candidatesById.set(p.id, p);
  }
  const ranked = [...candidatesById.values()].sort(
    (a, b) => Math.abs(a.paldexNo - pal.paldexNo) - Math.abs(b.paldexNo - pal.paldexNo),
  );
  return ranked.slice(0, n);
}

function buildFaqs(pal: Pal, pairCount: number): FaqEntry[] {
  const elements = pal.elements.join(" / ");
  const breedingLine = pairCount > 0
    ? `${pal.name} can be produced from ${pairCount} known parent pair${pairCount === 1 ? "" : "s"}, ranked on this page by obtainability.`
    : `Standard power-value breeding doesn't directly produce ${pal.name} in our current data. Special combinations may apply — check back as the dataset grows.`;
  const breedOnlyLine = pal.breedOnly
    ? `${pal.name} is breed-only: it doesn't appear in the wild and must be obtained through a specific parent pair.`
    : `${pal.name} can be encountered in the wild, but breeding for specific passives is usually faster than re-rolling captures.`;

  return [
    {
      question: `How do I get ${pal.name} in Palworld?`,
      answer: `${breedOnlyLine} ${breedingLine}`,
    },
    {
      question: `What element is ${pal.name}?`,
      answer: `${pal.name} is a ${elements} type Pal. Its primary element is ${pal.elements[0] ?? "Neutral"}, which determines its element-matched breeding pool.`,
    },
    {
      question: `What is ${pal.name}'s power value?`,
      answer: `${pal.name} has a power value of ${pal.powerValue}. The breeding engine uses this to compute the child of any pair as the floor of (parentA.powerValue + parentB.powerValue + 1) / 2 and pick the closest breedable Pal.`,
    },
    {
      question: `What's the best parent pair for ${pal.name}?`,
      answer:
        pairCount > 0
          ? `Pairs are ranked by obtainability on this page — lower Paldex numbers, matching primary elements, and avoiding breed-only intermediaries score higher. Use the planner to factor in your roster's existing passives.`
          : `Once parent pairs are known we'll list them here. In the meantime, the planner can search across your roster for any path that produces ${pal.name}.`,
    },
  ];
}
